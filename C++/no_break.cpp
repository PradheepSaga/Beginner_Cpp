#include <iostream>
using namespace std;

int main() {

	int n = 2;
	switch(n) {
		case 1: cout << "Summer" << endl;
		case 2: cout << "Winter" << endl;
		case 3: cout << "Spring" << endl;
		case 4: cout << "Autumn" << endl;
		case 5: cout << "monsoon" << endl;
	}
}